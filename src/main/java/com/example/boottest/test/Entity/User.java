package com.example.boottest.test.Entity;


public class User {

    /**
     * 姓名
     */
    private String Name;

    /**
     * 性别
     */
    private char Sex;

    /**
     * 年龄
     */
    private int Age;

    /**
     * 发送内容
     */
    private String Content;

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public char getSex() {
        return Sex;
    }

    public void setSex(char sex) {
        Sex = sex;
    }

    public int getAge() {
        return Age;
    }

    public void setAge(int age) {
        Age = age;
    }

    public String getContent() {
        return Content;
    }

    public void setContent(String content) {
        Content = content;
    }





}
